    ("Бонус: 12.345 рублей", "Бонус: 12345 ₽"),
    ("Доход: 15.3 тыс ₽", "Доход: 15300 ₽"),
    ("Доход: 2 500 000 ₽", "Доход: 2500000 ₽"),
    ("Бонус: 12,345 рублей", "Бонус: 12345 ₽"),
    ("от 3.000₽ до 35.000₽", "от 3000₽ до 35000₽"),
